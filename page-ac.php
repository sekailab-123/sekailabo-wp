            <h2><span id="i-12">請求情報</span></h2>
            <p>Shopifyストアでの購入および請求書の決済手段を追加、変更が行えます。また、Shopifyの料金プランや有料アプリなど、定期的に請求されるアイテムが確認できます。請求書や明細書に関する過去の記録についても確認、ダウンロードが可能です。</p>
            <h2><span id="i-13">ユーザーと権限</span></h2>
            <p>ストア管理画面へのログイン権限を編集することができます。スタッフやデザイナー、開発者がその程度管理画面の機能を使用できるか、制限を設けることも可能です。</p>
            <h2><span id="i-14">決済</span></h2>
            <p>ストア内の決済方法に関する編集が行えます。デフォルトのShopifyペイメントをはじめ、PayPalや外部決済システムなど、導入したい決済方法を設定できます。</p>
            <h2><span id="i-15">チェックアウト</span></h2>
            <p>チェックアウト時に購入者が入力する情報の編集が行えます。また、チェックアウト画面のデザインカスタマイズや、カゴ落ちメールの通知編集などもこちらで行えます。</p>
            <h2><span id="i-16">配送と配達</span></h2>
            <p>配送料の設定が行えます。全国一律であったり地域別で分けたりなど、運営者の意向に合わせて自由な価格をカスタムできます。</p>
