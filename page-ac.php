            <p>アカウント情報の管理や別ストアへの移行、言語の変更などが行えます。アカウント情報に関しては、名前や写真、電話番号といった細かい設定の変更が可能です。</p>
            <h2><span id="i-29">ストア</span></h2>
            <p>Shopifyストアを一覧表示させた上で、別ストアへの切り替え、作成が行えます。こちらではShopifyパートナーアカウントから作成したものを含む、全ストアへの移行が可能です。</p>
